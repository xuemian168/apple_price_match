export const getPricingByCountryAndPlan = (
  country: string,
  plan: string
): iCloudPricing | undefined => {
  return sampleiCloudPricing.find(
    (pricing) => pricing.country === country && pricing.plan === plan
  );
};

export const getPricingByPlan = (plan: string): iCloudPricing[] => {
  return sampleiCloudPricing.filter((pricing) => pricing.plan === plan);
};

export const getPricingByCountry = (country: string): iCloudPricing[] => {
  return sampleiCloudPricing.filter((pricing) => pricing.country === country);
};